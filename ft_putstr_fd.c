#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	size_t	i;

	if (!s)
		return ;
	i = 0;
	if (fd >= 0)
	{
		while (i < ft_strlen(s))
		{
			write(fd, &s[i], 1);
			i++;
		}
	}
}
